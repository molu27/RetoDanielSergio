package reto;

public class funciones1 {
public static int sumar(int n1, int n2) {
	return n1 + n2;
	
}
public static int restar(int n1, int n2) {
	return n1 - n2;
}
public static boolean esPar (int n1) {
	if (n1 % 2 == 0) {
		return true;
	}
		return false;
}
public static boolean esImpar (int n1) {
	if (n1 % 2 != 0) {
		return true;
	}
		return false;
}


}

